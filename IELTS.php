                            language abilities for the UK Visa and Immigration (UKVI) application purposes can opt for IELTS UKVI test format.</p>
                        <p><strong>IELTS TEST TYPES / IELTS TEST FORMAT</strong></p>
                        <p>The IELTS exam can be taken<br>
                            online (Computer-delivered IELTS)<br>
                            offline (IELTS paper-based test).</p>
